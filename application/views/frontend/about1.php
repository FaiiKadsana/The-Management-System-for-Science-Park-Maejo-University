          <ul >
           <li >เพื่อเป็นพื้นที่ และสะพานที่เชื่อมโยงองค์ความรู้ด้านวิทยาศาสตร์
             เทคโนโลยีและนวัตกรรม ของมหาวิทยาลัย ออกไปสู่การใช้ประโยชน์โดยเฉพาะ
             เชิงพาณิชย์กับโลกภายนอก โดยเป็นหน่วยงานที่สามารถ<br>เข้าใจทั้งวิธีคิดของนักวิชาการมหาวิทยาลัย 
