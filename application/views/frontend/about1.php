        <h4 class='title-header1 org'>วัตถุประสงค์ในการจัดตั้งจากการมีอุทยานวิทยาศาสตร์</h4>

        <p> &nbsp&nbsp&nbsp&nbsp&nbsp&nbsp ประโยชน์ที่มหาวิทยาลัยจะได้รับจากอุทยานวิทยาศาสตร์  
          และผลกระทบที่เกิดกับเศรษฐกิจและสังคมจากการมีอุทยานวิทยาศาสตร์ 
