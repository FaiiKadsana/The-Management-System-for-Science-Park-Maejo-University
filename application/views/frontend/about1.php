              มาใช้ประโยชน์กับผู้ประกอบการที่ต้องการเริ่มพัฒนางานวิจัยด้านวิทยาศาสตร์และเทคโนโลยี
              ด้วยตัวเองเพื่อช่วยลด<br>ค่าใช่จ่ายในด้านการลงทุนเครื่องมือทางวิทยศาสตร์ในระยะเริ่มต้น
              ซึ่งจะช่วยให้ภาคเอกชนลงทุนในงานวิจัยด้านวิทยาศาสตร์และเทคโนโลยีมากขึ้นส่งผลกระทบต่อ
              การเพิ่มเปอร์เซ็นต์งานวิจัยต่อ GDP <br>ของประเทศให้สูงขึ้นตามนโยบายของรัฐบาล</li><br>

