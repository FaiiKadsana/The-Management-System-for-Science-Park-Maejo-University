      <h4 class='title-header1 org'>อุทยานวิทยาศาสตร์มหาวิทยาลัยแม่โจ้</h4><br>

      <p> &nbsp&nbsp&nbsp&nbsp&nbsp&nbsp อุทยานวิทยาศาสตร์ มหาวิทยาลัยแม่โจ้ อยู่ภายใต้การกำกับตั้งสภามหาวิทยาลัย อธิการบดี
        มีคณะกรรมการอำหน่วยการบริการทรัพย์สินและหน่วยงานในกำกับ เป็นคณะกรรมการอำนวยการ 
        และมีคณะกรรมการอุทยานวิทยาศาสตร์ หมาวิทยาลัยแม่โจ้ เป็นคณะกรรมการดำเนินงาน 
        การดำเนินงานในฝ่ายปฏิบัติมีผู้อำนวยการ และรองผู้อำนวยการเป็นฝ่ายบริหารงาน 
        โดยมีผู้จัดการเป็นหัวหน้าฝ่ายปฏิบัติงานของอุทยานวิทยาศาสตร์ ซึ่งแบ่งฝ่ายต่างๆ 
        ที่เกี่ยวข้อง ได้แก่ สำนักงานกลาง ศูนย์ความร่วมมืออุตสาหกรรม ศูนย์ห้องปฏิบัติการและโรงงานต้นแบบ ศูนย์บริหารจัดการทรัพย์สินทางปัญญาและบ่มเพาะธุรกิจ และศูนย์ออกแบบวัตกรรม</p><br>

        <p> &nbsp&nbsp&nbsp&nbsp&nbsp&nbsp ทั้งนี้ได้กำหนดโครงการที่มุ่งเน้น ได้แก่นวัตกรรมข้าวไทยเพิ่มมูลค่าสู่ตลาดโลก 
          และมีอุตสาหกรรมเป้าหมาย 4 กลุ่มได้แก่</p><br>
          <p>1) อุตสาหกรรมในโซ่คุณค่าของพืชผัก และผลไม้ สมุนไพรเมืองเหนือ และข้าว</p> 
          <p>2) อุตสาหกรรม IT software และ Digital Content</p>
          <p>3) อุตสาหกรรมด้านการแพทย์และเทคโนโลยีชีวภาพ</p>
          <p>4) อุตสาหกรรมด้านเทคโนโลยีพลังงานทดแทนสิ่งแวดล้อม และวัสดุด้านพลังงาน </p><br>

          <h4 class='title-header1 org'></h4>
          <p> &nbsp&nbsp&nbsp&nbsp&nbsp&nbsp ปัจจุบันงานสำนักงานเลขนุการคณะกรรมการส่งเสริมจากอุทยานวิทยาศาสตร์ (สอว.)
            สำนักงานปลัดกระทรวงวิทยาศาสตร์ละเทคโนโลยี ได้เข้ามาดูแลประสานการดำเนินงาน
            ของอุทยาวิทยาศาสตร์ ภูมิภาค โดยกำหนดกิจกรรมการดำเนินงานในแต่ละปีงบประมาณจำนวน
            4 กิจกรรมหลัก ได้แก่ </p><br>

            <ul>
             <li > การพัฒนาบริการอุทยานวอทยาศาสตร์ Service Platform) </li><br>
             <li > การบ่มเพาะธุรกิจ วทน. (Incubation Platform) </li><br>
             <li > แผนการพัฒนาศักยภาพทางการวิจัยและพัฒนาของเอกชนในพื้นที่ (IRTC)</li><br>
             <li > การวิจัยร่วมกับภาคเอกชน (Co-Research)</li><br>
