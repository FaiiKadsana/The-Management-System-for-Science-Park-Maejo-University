            <p> &nbsp&nbsp&nbsp&nbsp&nbsp&nbsp อุตสาหกรรมมุ่งเน้นในการดำเนินงานอุทยานวิทยาศาสตร์ภาคเหนือคือ การใช้เทคโนโลยีเข้าไปพัฒนาและยกระดับข้าวไทยเพื่อเพิ่มมูลค่าตลอดห่วงโซ่คุณค่า (Value Chain)
