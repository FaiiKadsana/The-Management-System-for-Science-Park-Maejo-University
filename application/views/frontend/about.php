      <p> &nbsp&nbsp&nbsp&nbsp&nbsp&nbsp อุทยานวิทยาศาสตร์  (Science Park)  คือ สถานที่ที่สนับสนุนการวิจัยและพัฒนา ทางวิทยาศาสตร์
        เทคโนโลยี และนวัตกรรมร่วมกันของภาคการศึกษา ภาค
        อุตสาหกรรม และภาครัฐบาล เพื่อออกสู่เชิงพาณิชย์โดยมีวัตถุประสงค์เพื่อเพิ่ม
        มูลค่าการผลิตภัณฑ์ และเพิ่มขีดความสามารถในการแข่งขันของภาคเอกชน 
        นำไปสู่การพัฒนาทางเศรษฐกิจและคุณภาพชีวิตของคนในพื้นที่และประเทศ
        อุทยานวิทยาศาสตร์นิยามสั้น ๆ ได้ว่า “นิคมวิจัย”</p>

