             <h4 class='title-header1 org'>บริการ</h4>

             <p>อุทยานวิทยาศาสตร์ภาคเหนือให้บริการภายใต้ 4 โปรแกรมหลัก ได้แก่</p><br>
             <h4 >1. โครงการพัฒนาบริการของอุทยานวิทยาศาสตร์</h4>
             <ul >
               <li >การพัฒนาศักยภาพของห้องปฏิบัติการสำหรับการให้บริการแก่ภาคอุตสาหกรรม</li>
               <li >การให้บริการออกแบบนวัตกรรมโดยศูนย์ออกแบบนวัตกรรม</li>
               <li >การให้บริการด้านการบริหารจัดการทรัพย์สินทางปัญญาแก่ภาคอุตสาหกรรม</li>
               <li >การให้บริการฐานข้อมูลโครงสร้างพื้นฐานภาครัฐด้านวิทยาศาสตร์และเทคโนโลยี</li>
               <li >การให้บริการและดำเนินการของสำนักงานความร่วมมืออุตสาหกรรม</li>
             </ul><br>

             <h4 >2. โครงการบ่มเพาะธุรกิจวิทยาศาสตร์ เทคโนโลยี และนวัตกรรม</h4>
             <ul >
               <li >บริการบ่มเพาะธุรกิจเทคโนโลยีแก่ผู้สนใจเป็นผู้ประกอบการธุรกิจเทคโนโลยี</li>
               <li >การจัดแข่งขันกิจกรรม Research to Market เพื่อให้นักศึกษาได้ตระหนักถึงการเป็นผู้ประกอบการธุรกิจเทคโนโลยี</li>
             </ul><br>
             <h4 >3. โครงการพัฒนาขีดความสามารถทางเทคโนโลยีและวิจัยของภาคเอกชนในพื้นที่</h4>
             <p> &nbsp&nbsp&nbsp&nbsp&nbsp&nbsp เพื่อยกระดับความสามารถทางการวิจัยและพัฒนาเทคโนโลยีของผู้ประกอบการในภาคอุตสาหกรรมต่าง ๆ เพื่อกระตุ้นและผลักดันให้ภาคอุตสาหกรรมได้ใช้ทรัพยากรทางด้านวิทยาศาสตร์และเทคโนโลยี ไม่ว่าส่วนของอุปกรณ์-เครื่องมือ หรือบุคลากร-ผู้เชี่ยวชาญ ของภาคการศึกษาอย่างคุ้มค่า</p><br>
             <h4 >4. โครงการวิจัยร่วมกับภาคเอกชน</h4>
             <p> &nbsp&nbsp&nbsp&nbsp&nbsp&nbsp เพื่อชักจูงให้ภาคเอกชนเห็นความสำคัญและหันมาสนใจใช้การวิจัยและพัฒนาด้านวิทยาศาสตร์และเทคโนโลยีในการเพิ่มมูลค่าและสร้างความเข้มแข็งให้กับธุรกิจ นำไปสู่การต่อยอดการลงทุนวิจัยและพัฒนาในภาคเอกชนและเกิดผลกระทบเชิงบวกอย่างมีนัยสำคัญในระบบเศรษฐกิจฐานความรู้ อีกทั้งเพื่อเตรียมความพร้อมของภาคเอกชนในการเป็นลูกค้าของอุทยานวิทยาศาสตร์ภาคเหนือในอนาคต</p>
